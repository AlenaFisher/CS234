    //Finds an item in the Inventory table
    private void findItem() {
        
        DefaultTableModel sorter = (DefaultTableModel)tblInventory.getModel();
        TableRowSorter<DefaultTableModel> sorter1 = new TableRowSorter<>(sorter);
        tblInventory.setRowSorter(sorter1);
        sorter1.setRowFilter(RowFilter.regexFilter(txtLbID.getText()));
        
    }
