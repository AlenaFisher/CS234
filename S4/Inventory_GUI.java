    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //Saves and uploads data to the Inventory table
        try {
            System.out.println("Reading in data");
            FileInputStream file = new FileInputStream("inventory.dat");
            ObjectInputStream input = new ObjectInputStream(file);
            ArrayList<String> tableData = (ArrayList<String>)input.readObject();
            
            input.close();
            file.close();
            
            DefaultTableModel model = (DefaultTableModel)tblInventory.getModel();
            for(int i=0; i<tableData.size(); i++)
            {
                String[] row = tableData.get(i).split(",");
                model.addRow(row);
            }
            JOptionPane.showMessageDialog(this, "Data loaded successfully");
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        //Saves and uploads data to Inventory table
        DefaultTableModel model = (DefaultTableModel)tblInventory.getModel();
        
        ArrayList<String> tableData = new ArrayList<>();
        
        for(int i=0; i<model.getRowCount();i++)
        {  
            String id = model.getValueAt(i, 0).toString();
            String name = model.getValueAt(i, 1).toString();
            String price = model.getValueAt(i, 2).toString();
            String quantity = model.getValueAt(i, 3).toString();
            
            String row = id + "," + name + "," + price + "," + quantity;
            tableData.add(row);
            System.out.println(row);
        }
       
//        Vector<Vector> tableData = model.getDataVector();
//        
        try{
            FileOutputStream file = new FileOutputStream("inventory.dat");
            ObjectOutputStream output = new ObjectOutputStream(file);
            
            output.writeObject(tableData);
            
            output.close();
            file.close();

        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

